package sec02.exam01;

public class IntegerLiteralExample {
	public static void main(String[] args) {
		int v1 = 0b1011;
		int v2 = 0206;
		int v3 = 365;
		int v4 = 0xB3;
		System.out.println("v1 = " + v1);
		System.out.println("v2 = " + v2);
		System.out.println("v3 = " + v3);
		System.out.println("v4 = " + v4);
	}

}
